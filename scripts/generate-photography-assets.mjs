import { mkdir, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const photographyDir = path.join(projectRoot, "public", "photography");
const outputFile = path.join(projectRoot, "src", "photography-assets.generated.js");

const imageExtensions = new Set([".avif", ".gif", ".jpeg", ".jpg", ".png", ".svg", ".webp"]);
const videoExtensions = new Set([".m4v", ".mov", ".mp4", ".webm"]);

function getMediaType(fileName) {
  const extension = path.extname(fileName).toLowerCase();

  if (imageExtensions.has(extension)) {
    return "image";
  }

  if (videoExtensions.has(extension)) {
    return "video";
  }

  return null;
}

function getAltText(fileName) {
  const nameWithoutExtension = path.basename(fileName, path.extname(fileName));
  const readableName = nameWithoutExtension
    .replace(/[._-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();

  return readableName ? `Photography item ${readableName}` : "Photography gallery item";
}

async function readPhotographyFiles() {
  try {
    const entries = await readdir(photographyDir, { withFileTypes: true });
    return entries
      .filter((entry) => entry.isFile())
      .map((entry) => entry.name)
      .filter((fileName) => getMediaType(fileName))
      .sort((first, second) => first.localeCompare(second, undefined, { numeric: true, sensitivity: "base" }));
  } catch (error) {
    if (error.code === "ENOENT") {
      await mkdir(photographyDir, { recursive: true });
      return [];
    }

    throw error;
  }
}

const files = await readPhotographyFiles();
const photographyAssets = files.map((fileName) => ({
  type: getMediaType(fileName),
  src: `/photography/${fileName}`,
  alt: getAltText(fileName),
}));

const source = `// Generated by scripts/generate-photography-assets.mjs.
// Add or remove media in public/photography, then run npm run build.

export const photographyAssets = ${JSON.stringify(photographyAssets, null, 2)};
`;

await writeFile(outputFile, source);

console.log(`Generated ${photographyAssets.length} photography asset${photographyAssets.length === 1 ? "" : "s"}.`);
